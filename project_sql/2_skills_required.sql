-- Using the top 10 highest-paying Data Analyst jobs result set.

WITH top_paying_jobs AS
(
    SELECT
        *
    FROM
        (
        SELECT
        jp.job_id,
        job_title,
        cd.name AS company_name,
        salary_year_avg,
        RANK() OVER(ORDER BY salary_year_avg DESC) as rank
    FROM
        job_postings_fact jp
    JOIN company_dim cd
        ON jp.company_id = cd.company_id
    WHERE
        job_title_short = 'Data Analyst'
    AND job_location = 'Anywhere'
    AND salary_year_avg IS NOT NULL
        )x
    WHERE x.rank <= 10
)

SELECT tp.*,
    sd.skills
FROM top_paying_jobs tp
JOIN skills_job_dim sj ON tp.job_id = sj.job_id
JOIN skills_dim sd ON sj.skill_id = sd.skill_id;


